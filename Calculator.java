public class Calculator {
    int number1;
    int number2;

    int add (){
        return number1 + number2;
    }

    int substract (){
        return number1 - number2;
    }

    int multiply (){
        return number1 *  number2;
    }

    float div (){
        return (float)number1 / (float)number2;
    }

}
